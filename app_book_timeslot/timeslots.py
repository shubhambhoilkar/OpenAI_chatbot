from datetime import datetime, timedelta

def generate_timeslots():
    schedule = {}
    current = datetime.now()
    days_added = 0

    while len(schedule) < 10:  # 2 weeks, Mon-Fri (skip Sat/Sun)
        date_str = current.strftime("%Y-%m-%d")
        weekday = current.weekday()
        if weekday < 5:  # 0=Mon, ..., 4=Fri
            schedule[date_str] = []
            start_hour = 9
            end_hour = 18
            slot_duration = 90  # minutes

            slot_start = datetime(current.year, current.month, current.day, start_hour, 0)
            while slot_start.hour < end_hour:
                slot_end = slot_start + timedelta(minutes=slot_duration)
                slot_label = f"{slot_start.strftime('%H:%M')} - {slot_end.strftime('%H:%M')}"
                schedule[date_str].append({
                    "time": slot_label,
                    "status": "available",
                    "booked_by": None
                })
                slot_start = slot_end

            days_added += 1
        current += timedelta(days=1)

    return schedule

# The main schedule (acts as a database)
TIMESLOT_DB = generate_timeslots()

